-- Vehicle Blacklist
AddEventHandler('entityCreating', function(entity)
    local model = GetEntityModel(entity)
    local pOwner = NetworkGetEntityOwner(entity)
    for i=1, #BlacklistedModels do 
        if model == GetHashKey(BlacklistedModels[i]) then
            CancelEvent()
            break
        end
    end
end)

